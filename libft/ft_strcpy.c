#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	int	u;

	u = 0;
	while (src[u] != '\0')
	{
		dst[u] = src[u];
		u++;
	}
	dst[u] = '\0';
	return (dst);
}

#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*u;

	u = b;
	i = 0;
	while (i < len)
	{
		u[i] = (char)c;
		i++;
	}
	return (b);
}
